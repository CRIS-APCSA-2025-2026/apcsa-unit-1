/*
 * Arithmetic Operations
 */
public class MinMaxSizes {
    public static void main(String args[]) {
        System.out.println("Integer.SIZE : " + Integer.SIZE);
        System.out.println("Integer.MIN_VALUE : " + Integer.MIN_VALUE);
        System.out.println("Integer.MAX_VALUE : " + Integer.MAX_VALUE);
        System.out.println("Double.SIZE : " + Double.SIZE);
        System.out.println("Double.MIN_VALUE : " + Double.MIN_VALUE);
        System.out.println("Double.MAX_VALUE : " + Double.MAX_VALUE);
        //System.out.println("Boolean.SIZE : " + Boolean.SIZE);
        System.out.println("Character.SIZE : " + Character.SIZE);
        System.out.println("Character.MIN_VALUE : "
				+ (int) Character.MIN_VALUE + " => " + Character.MIN_VALUE);
        System.out.println("Character.MAX_VALUE : "
				+ (int) Character.MAX_VALUE + " => " + Character.MAX_VALUE);
    }
}
